<template>
    <input
        class="form-control"
        type="text"
        v-model="maleMsg"
        placeholder="Male_Message"
    />
</template>

<script>
export default {
    props: {
        editUser: {
            type: Object,
            default: () => {}
        }
    },
    computed: {
        maleMsg: {
            get() {
                return this.editUser.maleMsg
            },
            set(newValue) {
                this.$emit('input', newValue)
            }
        }
    }
}
</script>